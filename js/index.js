function enterNav(){
    var labels = window.document.querySelector('.instaLabels');
    labels.style.visibility = "visible";
    labels.style.opacity = '100';

}
function leaveNav(){
    var labels = window.document.querySelector('.instaLabels');
    
    labels.style.opacity = "0";
    labels.style.visibility = "hidden";
}

